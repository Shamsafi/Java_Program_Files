public class Assignment_operator {
    public static void main(String[] args) {
        int number =20;

        System.out.println("Initial number:- " +number);

        //+=
        number += 5;
        System.out.println("After += number is:- " +number);

        //-=
        number -= 5;
        System.out.println("After -= number is:- " +number);

        //*=
        number *= 5;
        System.out.println("After *= number is:- " +number);

        // /=
        number /= 5;
        System.out.println("After /= number is:- " +number);

        // %=
        number %= 5;
        System.out.println("After %= number is:- " +number);
    }
}
