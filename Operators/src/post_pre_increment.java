public class post_pre_increment {
    public static void main(String[] args) {
        int a = 5;

        System.out.println("Initial number:- " + a);
        System.out.println("Pre increment:- " + (++a));
        System.out.println("After pre:- " + a);

        int  number = 5;
        System.out.println("Initial number:- " + number);
        System.out.println("Post increment:- " + (number++));
        System.out.println("After pre:- " + number);
    }
}
